// Anonymous Function
var anonymousFunction = function(a, b) {
    // This is an anonymous function assigned to a variable 'anonymousFunction'
    // It also takes two parameters 'a' and 'b'
    console.log("Anonymous Function result: " + (a * b)); // It multiplies 'a' and 'b' and logs the result
};

anonymousFunction(5, 10); // Call the anonymous function with arguments 5 and 10
