let a = 5, b = 3, c = 8, d = 2, e = 10;

// Addition
let additionResult = a + b;  // Adds a and b
console.log(additionResult); // Output: 8 (5 + 3)

// Subtraction
let subtractionResult = a - c;  // Subtracts c from a
console.log(subtractionResult); // Output: -3 (5 - 8)

// Multiplication
let multiplicationResult = b * c;  // Multiplies b and c
console.log(multiplicationResult); // Output: 24 (3 * 8)

// Division
let divisionResult = c / d;  // Divides c by d
console.log(divisionResult); // Output: 4 (8 / 2)

// Modulus (Remainder)
let modulusResult = c % b;  // Finds the remainder when c is divided by b
console.log(modulusResult); // Output: 2 (8 % 3)

// Exponentiation (Power)
let exponentiationResult = b ** e;  // Raises b to the power of e
console.log(exponentiationResult); // Output: 59049 (3 ** 10)


