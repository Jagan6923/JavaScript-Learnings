let a = 5, b = 3, c = 8, d = 5;

// raltional operator or comparison operator

// Greater than (>)
let result1 = a > b;  // Checks if a is greater than b
console.log(result1); // Output: true (5 > 3)

// Less than (<)
let result2 = a < b;  // Checks if a is less than b
console.log(result2); // Output: false (5 < 3)

// Greater than or equal to (>=)
let result3 = a >= c;  // Checks if a is greater than or equal to c
console.log(result3); // Output: false (5 >= 8)

// Less than or equal to (<=)
let result4 = b <= c;  // Checks if b is less than or equal to c
console.log(result4); // Output: true (3 <= 8)

// Equality (==)
let result5 = a == d;  // Checks if a is equal to d (value comparison)
console.log(result5); // Output: true (5 == 5)

// Strict equality (===)
let result6 = a === d;  // Checks if a is equal to d and both are of the same type (value and type comparison)
console.log(result6); // Output: true (5 === 5)

// Inequality (!=)
let result7 = a != b;  // Checks if a is not equal to b (value comparison)
console.log(result7); // Output: true (5 != 3)

// Strict inequality (!==)
let result8 = a !== c;  // Checks if a is not equal to c or they are not of the same type (value and type comparison)
console.log(result8); // Output: true (5 !== 8)
