// Function to calculate the square of a number
function square(num) {
    return num * num; // Returns the square of the input number
}

let result = square(4); // Call the function and store the result in 'result'
console.log(result); // Output: 16 (Result of 4 * 4)

// Function to check if a number is even
function isEven(num) {
    return num % 2 === 0; // Returns true if the number is even, false otherwise
}

console.log(isEven(4)); // Output: true (4 is even)
console.log(isEven(5)); // Output: false (5 is not even)
